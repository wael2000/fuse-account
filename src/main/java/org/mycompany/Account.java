package org.mycompany;

public class Account {

	private int id;
	private String name;
	private String category;
	int getId() {
		return id;
	}
	void setId(int id) {
		this.id = id;
	}
	String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	String getCategory() {
		return category;
	}
	void setCategory(String category) {
		this.category = category;
	}
	
}
